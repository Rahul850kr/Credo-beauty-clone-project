     function filterbrand() {
        var selected = document.querySelector("#s1").value;
        if (selected != 0) {
          var filteredbrandList = fragranceData.filter(function (elem) {
            return elem.brandname == selected;
          });
        }
        if (selected == 0) {
          var filteredbrandList = fragranceData.filter(function (elem) {
            return elem;
          });
        }
        displayData(filteredbrandList);
      }

      function sortPrice() {
        var selected = document.querySelector("#s6").value;
    
        if (selected == "low") {
          fragranceData.sort(function (a, b) {
            return a.price - b.price;
          });
        }
        if (selected == "high") {
          fragranceData.sort(function (a, b) {
            return b.price - a.price;
          });
        }
        if (selected == "A") {
          fragranceData.sort(function (a, b) {
            return a.productName.charCodeAt(0) - b.productName.charCodeAt(0);
          });
        }
        if (selected == "Z") {
          fragranceData.sort(function (a, b) {
            return b.productName.charCodeAt(0) - a.productName.charCodeAt(0);
          });
        }
        displayData(fragranceData);
      }

